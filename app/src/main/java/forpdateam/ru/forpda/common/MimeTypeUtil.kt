    @JvmStatic
    fun getType(nameOrExtension: String): String? {
        if (types.isEmpty()) init()
        val extension = getExtension(nameOrExtension)
        val type = types[extension]
        if (BuildConfig.DEBUG) Timber.d("MimeTypeUtil input=$nameOrExtension ext=$extension type=$type")
        return type
    }

    @JvmStatic
    fun getExtension(name: String): String {
        var extension = name
        val cut = name.lastIndexOf('.')
        if (cut != -1) {
            extension = extension.substring(cut + 1)
        }
        return extension
    }

    @JvmStatic
    fun isImage(nameOrExtension: String): Boolean {
        val type = getType(nameOrExtension) ?: return false
        return type.contains("image/")
    }
